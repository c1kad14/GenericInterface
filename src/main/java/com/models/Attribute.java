package com.models;

public class Attribute {
    private Node node;
    private String name;
    private String value;

    public Node getNode() {
        return node;
    }

    public void setNode(Node nodeName) {
        this.node = node;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
